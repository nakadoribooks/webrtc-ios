import UIKit

func windowFrame()->CGRect{
    return UIScreen.main.bounds
}

func windowSize()->CGSize{
    return windowFrame().size
}

func windowWidth()->CGFloat{
    return windowSize().width
}

func windowHeight()->CGFloat{
    return windowSize().height
}

